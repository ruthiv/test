package homework;

public class HelloWorld {

	public static void main(String[] args) {
		for(int i = 0; i<=10 ; i++) {
			System.out.println("Hello World");
		}
		for(int i = 10; i>=10 ; i--) {
			System.out.println("Hello World");
		}
		int i=0;
		while(i<=10) {
			System.out.println("Hello World");
			i++;
		}
		i=10;
		while(i>=0) {
			System.out.println("Hello World");
			i--;
		}

	}

}
