package c.flow.control.loops;

import java.util.Scanner;

public class Demo3While {    

	public static void main(String[] args) {
		for(int a = 0; a<=5; a++) {
			System.out.println(a);
			
		}
		System.out.println("______________");
		for(int a = 5; a>=0; a--) {
			System.out.println(a);
			
		}
		System.out.println("______________");
		for(int a = 0; a<=10; a+=2) {
			System.out.println(a);
			
		}

		

	}

}
